print("คำนวนหาพื้นที่สี่เหลี่ยมจตุรัส")
square = int(input("ใส่ความยาวด้าน: "))
print(f"พื้นที่สี่เหลี่ยมจตุรัสนี้คือ {square * square}\n")

print("คำนวนหาพื้นที่สี่เหลี่ยมผืนผ้า")
width = int(input("ใส่ความกว้าง: "))
length = int(input("ใส่ความยาว: "))
print(f"พื้นที่สี่เหลี่ยมผืนผ้าคือ {width * length}\n")

